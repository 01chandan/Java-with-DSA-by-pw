//Data types in Java

class datatype
{
	public static void main (String arg[])
	{
		char c = 'A';
		int num = 20;
		long l = 5000;
		float f = 20.30f;
		double d = 40.5;
		short s = 10;
		System.out.println("Char		| Data Type: " +c);
		System.out.println("Number		| Data Type: " +num);
		System.out.println("Number		| Data Tyoe: " +l);
		System.out.println("Floating	| Data Type: " +f);
		System.out.println("Floating	| Data Type: " +d);
		System.out.println("Number		| Data Type: " +s);	
	}
	
}
